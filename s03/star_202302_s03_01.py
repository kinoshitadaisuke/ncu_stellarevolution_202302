# proton mass
m_p = 1.673 * 10**-27

# electron mass
m_e = 9.109 * 10**-31

# comparison of proton mass and electron mass
ratio = m_p / m_e

# printing mass ratio of proton and electron
print (f'm_p     = {m_p:g} kg')
print (f'm_e     = {m_e:g} kg')
print (f'm_p/m_e = {ratio}')
